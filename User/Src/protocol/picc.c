// block process
/*
return
-1:err
0:i
1:r
2:wtx
3:pps
4:deselect
*/
block_preprocess_t picc_t4t_block_process(uint8_t *rx_buf, uint16_t rx_len)
{
    block_prologue_field_t *rx_prologue = (block_prologue_field_t *)rx_buf;
    block_preprocess_t b_pr = {0};

    if ((rx_prologue->PCB.val & BIT3) == BIT3)
    {
        if (!picc_check_cid(rx_prologue->cid))
        {
            b_pr.valid = 2; // cid err
            return b_pr;
        }
        b_pr.prologue.PCB.val |= BIT3;
        b_pr.prologue.cid = rx_prologue->cid;
    }

    switch (rx_prologue->PCB.val >> 6)
    {
    case 0: // i block
        if ((rx_prologue->PCB.val & PCD_CMD_I_BLOCK_Msk) != PCD_CMD_I_BLOCK)
        {
            b_pr.valid = 3; // i_pcb err
            return b_pr;
        }
        if (rx_prologue->PCB.i_pcb.chain_flg)
        { // R(ACK)
        }
        else
        {
            b_pr.valid = 1;
            b_pr.prologue.PCB.i_pcb.blk_t = 0;
            b_pr.prologue.PCB.i_pcb.fixed1 = 1;
            b_pr.prologue.PCB.i_pcb.blk_num = rx_prologue->PCB.i_pcb.blk_num;
            b_pr.data = &rx_buf[1 + rx_prologue->PCB.i_pcb.cid_flw + rx_prologue->PCB.i_pcb.nad_flw];
            b_pr.data_len = rx_len - 1 - rx_prologue->PCB.i_pcb.cid_flw - rx_prologue->PCB.i_pcb.nad_flw;
        }
        break;
    case 2: // r block
        if ((rx_prologue->PCB.val & PCD_CMD_R_BLOCK_Msk) != PCD_CMD_R_BLOCK)
        {
            b_pr.valid = 4; // r_pcb err
            return b_pr;
        }
        b_pr.valid = 1;
        if (rx_prologue->PCB.r_pcb.blk_num == picc_blk_num_get())
        {
            // block number match,send last i block
        }
        else
        {
            if (rx_prologue->PCB.r_pcb.nak_flg)
            {
                b_pr.prologue.PCB.r_pcb.blk_t = 2;
                b_pr.prologue.PCB.r_pcb.fixed0 = 0;
                b_pr.prologue.PCB.r_pcb.fixed1 = b_pr.prologue.PCB.r_pcb.fixed1_ = 1;
                b_pr.prologue.PCB.r_pcb.blk_num = picc_blk_num_get();
                b_pr.prologue.PCB.r_pcb.nak_flg = 0;
            }
        }
        break;
    case 3: // s block
        if ((rx_prologue->PCB.val & PCD_CMD_S_BLOCK_Msk) != PCD_CMD_S_BLOCK)
        {
            b_pr.valid = 5; // s_pcb err
            return b_pr;
        }
        if (rx_prologue->PCB.s_pcb.func == 3)
        { // wtx
        }
        else if (rx_prologue->PCB.s_pcb.func == 0)
        { // deselect
            b_pr.valid = 1;
            b_pr.prologue.PCB.s_pcb.blk_t = 3;
            b_pr.prologue.PCB.s_pcb.func = 0;
            b_pr.prologue.PCB.s_pcb.fixed0 = b_pr.prologue.PCB.s_pcb.fixed0_ = 0;
            b_pr.prologue.PCB.s_pcb.fixed1 = 1;
        }
        break;
    default:
        return b_pr;
    }
    return b_pr;
}

static void picc_t4t_rats_process(uint8_t *buf, uint16_t len)
{
    if (buf)
    {
        switch (buf[0])
        {
        case 0x50:
            picc_set_halt();
            break;
        case 0xe0:
            picc_cid_set(picc_run.rx_buf[1] & 0x0f);
            picc_run.rats.fsdi = au16FSD_CODE[((picc_run.rx_buf[1] >> 4) > 8) ? 8 : (picc_run.rx_buf[1] >> 4)];
            memcpy(picc_run.tx_buf, &picc_conf.ats, sizeof(struct picc_ats));
            picc_run.tx_len = picc_conf.ats.TL;
            picc_write(picc_run.tx_buf, picc_run.tx_len);
            NFC_LOG("PICC is Activated\r\n");
            picc_blk_num_set(1);
            picc_run_state(PICC_STATE_PPS);
            break;
        default:
            break;
        }
    }
}

void picc_set_rate(uint8_t rate)
{
    rate = rate & 0x3;
    picc_run.rate = rate;
    switch (rate)
    {
    case 0:
        pcd_set_rate('1', 'A');
        break;
    case 1:
        pcd_set_rate('2', 'A');
        break;
    case 2:
        pcd_set_rate('4', 'A');
        break;
    case 3:
        pcd_set_rate('8', 'A');
        break;
    }
}

static uint8_t picc_t4t_pps_process(uint8_t *buf, uint16_t len)
{
    uint8_t ret = 0;
    if (buf)
    {
        pps_t *pps = (pps_t *)buf;
        if ((picc_check_cid(pps->PPSS.bitmap.cid)) &&
            (pps->pps0 == 0x11))
        {
            picc_write(&pps->PPSS.val, 1);
            picc_set_rate(pps->pps1);
            ret = 1;
        }
        else
        {
            ret = 0;
        }
        picc_run_state(PICC_STATE_RECV_BLOCK);
    }
    return ret;
}

static void picc_t4t_process(void)
{
    block_preprocess_t prep = {0};
    if (picc_run_state_check(PICC_STATE_RECV_BLOCK))
    {
        prep = picc_t4t_block_process(picc_run.rx_buf, picc_run.rx_len - 2);
    }
    else if (picc_run_state_check(PICC_STATE_ACTIVE))
    {
        picc_t4t_rats_process(picc_run.rx_buf, picc_run.rx_len);
    }
    else if (picc_run_state_check(PICC_STATE_PPS))
    {
        if (!picc_t4t_pps_process(picc_run.rx_buf, picc_run.rx_len))
            prep = picc_t4t_block_process(picc_run.rx_buf, picc_run.rx_len - 2);
    }
    picc_t4t_apdu_process(&prep, &t4t_tag);
}

void picc_14443_4_enable(uint8_t en)
{
    if (en)
    {
        nfc_set_bit(10, 0x3A, 1, 1);
        nfc_clear_bit(9, 0x3D, 0x80, 1);
        picc_conf.app = picc_t4t_process;
        init_t4t_entity(&t4t_tag);
    }
    else
    {
        nfc_clear_bit(10, 0x3A, 1, 1);
        picc_conf.app = picc_t2t_process;
    }
}

