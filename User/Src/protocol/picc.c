    case 0x50:
        picc_set_halt();
        break;
    case 0x30:
        picc_write(t2t_mem[picc_run.rx_buf[1]], 16);
        break;
    case 0x60:
        picc_write(t2t_ver, 8);
        break;
    case 0x3C:
