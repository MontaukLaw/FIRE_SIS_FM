void handle_read(t4t_entity *tag, c_apdu_t *cmd, r_apdu_t *resp)
{
    uint16_t offset = (cmd->p1 << 8) | cmd->p2;

    if (offset + cmd->le.ext_le > tag->cur_file->size)
    {
        resp->sw = 0x6A84; // 地址越界
        return;
    }

    // 从存储块读取数据（支持跨块读取）
    resp->data = &tag->ndef_data[tag->cur_file->start_block * sizeof(ndef_uri_record) + offset];
    resp->len = cmd->le.ext_le;
    resp->sw = 0x9000; // 成功状态
}

file_entry *find_file(t4t_entity *tag, uint16_t fid)
{
    for (uint8_t i = 0; i < tag->file_count; i++)
    {
        if (tag->files[i].fid == fid)
            return &tag->files[i];
    }
    return NULL;
}

void handle_select(t4t_entity *tag, c_apdu_t *cmd, r_apdu_t *resp)
{
    if (cmd->lc.ext_lc == 2)
    {
        tag->cur_file = find_file(tag, cmd->data[0] << 8 | cmd->data[1]);
        if (tag->cur_file)
        {
            resp->sw = 0x9000; // 成功状态码
        }
    }
    else if (cmd->lc.ext_lc == 7 && memcmp(cmd->data, "\xD2\x76\x00\x00\x85\x01\x01", 7) == 0)
    {                      // NDEF应用选择
        resp->sw = 0x9000; // 成功响应
    }
    else
    {
        resp->sw = 0x6A82; // 文件未找到
    }
}

int parse_capdu(const uint8_t *apdu, uint16_t len, c_apdu_t *cmd)
{
    if (len < 4)
        return -1; // 基础头校验

    // 解析固定头
    cmd->cla = apdu[0];
    cmd->ins = apdu[1];
    cmd->p1 = apdu[2];
    cmd->p2 = apdu[3];
    uint8_t offset = 4;

    cmd->lc_type = 0; // 无Lc
    // 处理Lc/Data部分
    if ((offset + 1) < len && apdu[offset] != 0x00)
    { // 短Lc
        cmd->lc_type = 1;
        cmd->lc.short_lc = apdu[offset++];
        cmd->data = &apdu[offset];
        offset += cmd->lc.short_lc;
    }
    else if ((offset + 3) < len && apdu[offset] == 0x00)
    { // 扩展Lc
        cmd->lc_type = 2;
        cmd->lc.ext_lc = (apdu[offset + 1] << 8) | apdu[offset + 2];
        offset += 3;
        cmd->data = &apdu[offset];
        offset += cmd->lc.ext_lc;
    }

    // 处理Le部分
    if (offset < len)
    {
        if ((offset + 1) == len)
        { // 短Le
            cmd->le_type = 1;
            cmd->le.short_le = apdu[offset++];
        }
        else if ((offset + 2) == len)
        {
            if (cmd->lc_type != 2)
                return -3;
            cmd->le_type = 3;
            cmd->le.ext_le = (apdu[offset] << 8) | apdu[offset + 1];
            offset += 2;
        }
        else if ((offset + 3) == len)
        {
            if (cmd->lc_type != 1)
                return -4;
            if (apdu[offset] != 0)
                return -5;
            cmd->le_type = 2;
            cmd->le.ext_le = (apdu[offset + 1] << 8) | apdu[offset + 2];
            offset += 3;
        }
    }

    return (offset != len ? -5 : 0);
}

void picc_t4t_send_i(block_prologue_field_t *p, r_apdu_t *r)
{
    uint8_t len = 0;
    // PCB
    picc_run.tx_buf[len++] = p->PCB.val;
    // cid
    if (p->PCB.val & BIT3)
        picc_run.tx_buf[len++] = p->cid;
    // inf
    if (r->len)
    {
        memcpy(&picc_run.tx_buf[len], r->data, r->len);
        len += r->len;
    }
    // sw
    picc_run.tx_buf[len++] = (uint8_t)(r->sw >> 8);
    picc_run.tx_buf[len++] = (uint8_t)(r->sw & 0xff);
    picc_run.tx_len = len;

    picc_write(picc_run.tx_buf, picc_run.tx_len);

    picc_blk_num_set(p->PCB.val & BIT0);
}

static void picc_t4t_deselect(block_prologue_field_t *p)
{
    uint8_t len = 0;
    // PCB
    picc_run.tx_buf[len++] = p->PCB.val;
    // cid
    if (p->PCB.val & BIT3)
        picc_run.tx_buf[len++] = p->cid;

    picc_run.tx_len = len;

    picc_write(picc_run.tx_buf, picc_run.tx_len);
    picc_set_halt();
    pcd_set_rate('1', 'A');
}

void picc_t4t_apdu_process(block_preprocess_t *prep, t4t_entity *tag)
{
    r_apdu_t rapdu = {0};
    c_apdu_t capdu = {0};
    if (prep->valid == 1)
    {
        switch (prep->prologue.PCB.val >> 6)
        {
        case 0: // i block
            if (parse_capdu(prep->data, prep->data_len, &capdu))
            {
                rapdu.sw = 0x6700;
            }
            else
            {
                if (capdu.ins == 0xA4)
                {
                    handle_select(tag, &capdu, &rapdu);
                }
                else if (capdu.ins == 0xB0)
                {
                    handle_read(tag, &capdu, &rapdu);
                }
                else if (capdu.ins == 0xD0)
                {
                }
                else
                {
                    rapdu.sw = 0x6D00;
                }
                picc_t4t_send_i(&prep->prologue, &rapdu);
            }
            break;

        case 2:
            picc_t4t_send_r(&prep->prologue);
            break;

        case 3: // s block
            if (prep->prologue.PCB.s_pcb.func == 0)
            {
                picc_t4t_deselect(&prep->prologue);
            }
            break;
        }
        prep->valid = 0;
    }
}

